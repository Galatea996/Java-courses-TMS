/*
Необходимо вывести на экран числа от 5 до 1.При решении
используйте операцию декремента (--).
 */

public class OneToFive {
    public static void main(String[] args) {
        int i = 5;
        do {
            System.out.println(i);
            i--;

        } while (i > 0);

    }
}